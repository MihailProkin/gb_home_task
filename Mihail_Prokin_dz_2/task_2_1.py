"""
Выяснить тип результата выражений:
15 * 3
15 / 3
15 // 2
15 ** 2
"""
a = 15 * 3
b = 15 / 3
c = 15 // 2
d = 15 ** 2

print(f"Тип результата выражения 15 * 3 = {a} {type(a)}")
print(f"Тип результата выражения 15 / 3 = {b} {type(b)}")
print(f"Тип результата выражения 15 // 3 = {c} {type(c)}")
print(f"Тип результата выражения 15 ** 3 = {d} {type(d)}")
